def find_matching_knowledge(user_message: str) -> dict | None:
    """
    ユーザーのメッセージに合うナレッジを検索する。

    Args:
        user_message: ユーザーからのテキスト

    Returns:
        dict: マッチしたナレッジ、または None
    """
    message_lower = user_message.lower()

    for knowledge in KNOWLEDGE_BASE:
        for keyword in knowledge["keywords"]:
            if keyword in message_lower:
                logger.info(
                    f"ナレッジマッチ: [{knowledge['category']}] キーワード「{keyword}」"
                )
                return knowledge

    logger.info("ナレッジに該当なし")
    return None


def get_all_categories() -> list:
    """全ナレッジのカテゴリ一覧を返す（管理用）"""
    return [k["category"] for k in KNOWLEDGE_BASE]
